            <span>Higiene do sono</span>
            <br><br>
            O termo higiene do sono se refere a todos os fatores de hábitos de sono saudáveis. Esses fatores incluem:
            <br><br>
            <span>Regularidade:</span> tentativa de dormir e acordar nos mesmos horários todos os dias.<br><br>
            <span>Ingestão de cafeína:</span> tentativa de ingestão de cafeína às horas da manhã.<br><br>
            <span>Consumo de álcool:</span> limitar o consumo de álcool antes de dormir.<br><br>
            <span>Sua atmosfera de sono:</span>
            Um quarto silencioso e escuro não utilizado para atividades de lazer (TV, videogame, leitura, etc.); ruído branco está bem.<br>
            Uma temperatura confortável: pense em "Cachinhos Dourados", nem muito quente nem muito frio.<br>
            Uma superfície confortável para dormir: para a saúde física, a superfície mais firme na qual você ainda pode ter uma boa noite de sono.<br><br>
            <span>Hábitos e rotina:</span>
            Executar os mesmos hábitos de higiene física antes de dormir todas as noites.<br>
            Desligue telas, luzes, rádios, etc. pelo menos 30 minutos antes de ir para a cama.
            <br><br>
            <span>Tempo de tela e seu efeito no sono:</span>
            Assistir a telas que emitem luz azul (telas de computador, monitores de TV, tablets e telefones) diminuirá a produção de melatonina pelo corpo -
            o hormônio responsável por manter o ciclo sono-vigília.<br><br>
            Um estudo avaliou os efeitos do uso do iPad antes de dormir; os resultados sugerem uma diminuição da melatonina em até 50%.<br>
            O tempo de tela antes de dormir também reduz o sono REM, e os relatos desses efeitos podem durar até 2-3 dias.
            <br><br>
            <span>Luz azul e interferência de sono:</span>
            Uma vez que a escuridão é o fator impulsionador da produção de melatonina, todas as formas de luz terão um efeito negativo na produção.<br><br id="Content5">
            A luz natural, a luz LED e a luz azul artificial são as mais inibidoras da produção de melatonina, enquanto as lâmpadas vermelhas ou amarelas quentes
            seriam as menos inibidoras.


