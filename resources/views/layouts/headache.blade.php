            e estruturas do pescoço; por exemplo, dormir de bruços provavelmente o forçará a virar o pescoço para um lado ou para o outro.<br>
            Manter esta posição por longos períodos de tempo (ou seja, uma hora seria considerada um período prolongado) levará à hipertonia e a pontos de gatilho.
