            <span>Como se hidratar</span>
        </p>
        <p class="text-left">
            <br>
            Água rica em eletrólitos e minerais que auxiliam na absorção dos nutrientes pelas células é a combinação certa para manter o corpo hidratado. <br>
            A quantidade de água a ser consumida diariamente depende das características físicas de cada pessoa e fatores externos
            (como exercícios e condições climáticas). <br>O ideal é seguir orientação profissional para entender a dosagem correta, por mais comum
            que seja beber 2 litros de água por dia.<br><br>

            Há também outros meios para matar a sede que aumentam as possibilidades, por mais que não sejam opções completas...<br>

            <li>Água de coco natural:</li> Rica em potássio, cálcio e magnésio.<br>

            <li>Frutas e vegetais:</li> É uma escolha rica em água, fibras e nutrientes (os eletrólitos estão contidos em muitos ingredientes).<br>

            <li>Não refinados:</li> Adicionar sal não refinado (como sal do Himalaia e flor de sal) às
            bebidas pode melhorar a absorção de água nas células.

