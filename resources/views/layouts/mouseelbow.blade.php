            <span style="color: #0eb7b7; font-size:30px;">Introdução</span>
            <br>
            <br>
            O cotovelo do mouse é uma tensão excessiva que ocorre nos tendões extensores do pulso e nos dedos que se fixam na parte externa (lado do polegar) do cotovelo.
            <br><br>
            Os músculos envolvidos são os músculos extensores que abrem a mão e dobram o pulso para trás.
            <br><br id="Content3">
            Como o nome sugere, os jogadores de PC são mais propensos a sofrer esse ferimento devido ao uso do mouse.<br>
            Devido ao uso indevido e ao desequilíbrio, a orientação das fibras dos tendões extensores na parte externa do cotovelo é interrompida.<br> 
            Essa disfunção é chamada de tendinose , que é diferente de tendinite - um termo comum para essa condição. 
            Discutiremos essa diferença com mais detalhes abaixo.


        </p>
