<section class="container-fluid bg-eyestrain2-content">
    <article class="container" style="padding-bottom: 20px; padding-top: 20px;">
        <p class="text-center">
            <span>Como você pode corrigir sua postura?</span>
            <br><br>
            Conforme mencionado anteriormente, não existe uma “postura adequada”. <br>
            Mas, aqui estão algumas dicas para minimizar os riscos associados à sessão prolongada.
        </p>
        <div class="row justify-content-center">
            <img class="col-lg-8 align-self-center" src="/assets/img/postura3.png">
        </div>
        <p class="text-left">
            <br>
            <li>1) Parte superior do corpo</li><br>
            <span>Cabeça:</span> se sua cadeira de jogador tiver uma, descanse sua cabeça contra o encosto de cabeça.<br><br>
            <span>Pescoço:</span> para ajudar a limitar a dor postural no pescoço e as dores de cabeça do tipo tensional,
            mantenha a cabeça para trás e o queixo dobrado para que as orelhas fiquem na mesma linha dos ombros.<br><br>
            <span>Ombros:</span> evite encolher os ombros e arredondar os ombros. <br>Puxe as omoplatas para baixo e em direção uma à outra,
            e descanse a parte superior das costas no encosto da cadeira.<br><br>

            <li>2) Monitor de jogos</li><br>
            Como regra geral, o tamanho da tela em polegadas é a distância que você deve posicionar o monitor de seu rosto.<br>
            A distância do rosto, independentemente do tamanho da tela, não deve ser inferior a 20 polegadas.<br><br>

            O1/3 - 1/4 superior do monitor de jogos deve estar no nível dos seus olhos. <br>
            Portanto, o monitor será inclinado ligeiramente para cima para encontrar seus olhos.<br><br>

            <li>3) Apoios de braço</li><br>
            Defina os apoios de braço apenas o suficiente para que os cotovelos fiquem dobrados a aproximadamente 90 graus.<br><br>

            Seus braços devem repousar suavemente sobre os apoios de braço, em vez de serem apoiados.<br>
            Muita pressão do braço repousa em seus antebraços pode levar à síndrome do túnel cubital.<br><br>

            Se seus braços não estiverem descansando adequadamente, você também pode estar usando os músculos do ombro para mantê-los erguidos. <br>
            Isso pode levar a uma condição conhecida como Tendinite Bicipital.<br><br>

            <li>4) Posição da mesa</li>
        </p>
        <p class="text-center">
            <span>Jogadores de PC</span>
        </p>
        <p class="text-left">
            A altura da escrivaninha deve ser alta o suficiente para cobrir o topo das coxas.<br>
            A altura da escrivaninha também deve ser baixa o suficiente para que os antebraços fiquem paralelos ao chão e à mesa. <br>
            Se sua mesa for muito alta, seus cotovelos e/ou pulsos ficarão tensos para ultrapassar a altura da mesa.<br><br>

            Puxe sua cadeira em direção à mesa para ficar perto o suficiente do monitor para não precisar se inclinar para frente, seguindo as orientações
            <br><span>1) parte superior do corpo</span> e <span>2) monitor de jogos.</span><br><br>
        </p>
        <p class="text-center">
            <span style="color:#0eb7b7;">Jogadores de console</span>
        </p>
        <p class="text-left">
            A altura de sua mesa deve ser apropriada para seguir as orientações:<br> <span>1) parte superior do corpo</span>
            e <span>2) monitor de jogos.</span><br><br>
            Da mesma forma, para seguir as orientações:<br> <span>1) parte superior do corpo</span> e
            <span>2) monitor de jogos</span>, puxe sua cadeira em direção à mesa de forma que
            fique perto o suficiente do monitor para não precisar se inclinar para frente.<br><br>

            <li>5) Encosto</li><br>
            Se a sua cadeira de jogador tiver a opção, recline as costas do assento 90-120 graus.<br><br>

            Tente manter a parte inferior das costas pressionada contra a cadeira. Sentar longe do encosto pode levar a uma posição desleixada.<br><br>

            <li>6) Quadris e coxas</li><br>
            Sente-se com os quadris totalmente para trás na cadeira, de modo que suas costas fiquem pressionadas contra o encosto.<br><br>

            Mantenha as duas coxas retas na cadeira. <br><br>

            <li>7) Altura, pernas e pés da cadeira</li><br>
            A altura da cadeira deve permitir que seus pés repousem totalmente no chão, sem pressão adicional sobre as pernas. <br>
            Muito alto e seus pés podem balançar livremente. <br>Muito baixo e suas coxas não ficarão totalmente apoiadas no assento.<br><br>

            Seus joelhos devem estar dobrados aproximadamente 90 graus.<br>

            Descanse os dois pés apoiados no chão abaixo dos joelhos.<br><br>

        </p>
