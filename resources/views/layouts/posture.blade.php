            <br>
            As posturas sentadas podem criar forças superiores a 150% do peso corporal na coluna.<br>
            O aumento da pressão sobre os discos e ossos da coluna vertebral pode levar à degeneração precoce (ou seja, artrite ou “espondilose”),
            que então leva a mais problemas de saúde.<br><br>

            Um desses problemas de saúde é chamado de estenose, ou estreitamento do canal espinhal ou canais neurais, onde a medula espinhal e os
            nervos viajam e saem da coluna vertebral. <br><br>
            A estenose pode causar irritação da medula espinhal e da raiz nervosa, causando sintomas como dor, dormência, formigamento ou fraqueza nos braços, tronco ou pernas.
            <br><br>
        </p>
        <p class="text-center">
            <span>Postura Prolongada e Crônica</span>
        </p>
        <p class="text-left">
            <span>Posturas sentadas prolongadas</span> têm o potencial de criar rigidez ou desequilíbrio notável na musculatura. <br>
            O mais importante são os efeitos nas regiões do quadril e ombros do corpo.<br><br>

            <span>As posições crônicas</span> sentadas criam rigidez nos músculos da frente do corpo - os flexores do quadril e os músculos peitorais,
            entre outros - que tendem a puxar o corpo para a frente em uma posição flexionada.<br><br>

            Além disso, os músculos da parte de trás do corpo - que mantêm o corpo ereto contra a gravidade - tornam-se fracos e inibidos.
