            <span>Riscos de má postura</span>
        </p>
        <p class="text-left">
            <br>
            Você pode ter ouvido a frase, “sentar é a novidade que fumar”, na tentativa de enfatizar os riscos à saúde associados ao sentar-se por muito tempo.<br><br>

            Sinceramente, sentar é tão prejudicial quanto fumar? Não, absolutamente não. <br>
            Fumar ainda é significativamente pior do que sentar; no entanto, os riscos de ficar sentado por muito tempo não devem ser subestimados.<br><br>

            A má postura e a posição sentada prolongada aumentam o risco de:<br><br>

            Degeneração precoce (artrite) da coluna, ombros e quadris.<br><br>

            Deslocamento do disco (protuberância do disco, herniação, etc.).<br><br>

            <li>Síndromes de choque nervoso</li>
            <li>Síndrome do piriforme (ciática)</li>
            <li>Síndrome do túnel carpal</li>
            <li>Síndrome do túnel cubital</li>
            <li>Síndrome de saída torácica</li>
            <li>Síndromes posturais</li>
            <li>Síndrome cruzada superior</li>
            <li>Síndrome cruzada inferior</li>
            <li>Trombose venosa profunda</li>
