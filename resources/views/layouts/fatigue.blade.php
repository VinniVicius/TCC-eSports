        <p class="text-center">
            <span>Dicas pra previnir o cansaço</span>
        </p>
        <p class="text-left">
            <br>
            <li>Regularidade</li>
            Procure adormecer e acordar nos mesmos horários todos os dias; jet lag social é quando você vai dormir ou acorda em horários variáveis ​​-
            semelhante a viajar fora de fusos horários, exceto sem a viagem.<br><br>

            <li>Continuidade</li>
            Claro, é melhor dormir a noite toda em vez de interromper o sono; isso pode exigir uma atualização da sua higiene do sono.<br><br>

            <li>Duração</li>
            Oito horas de sono tem sido a recomendação clássica para a duração do sono e certamente é a adequada; também recomendamos tentar dormir 8 horas por noite.<br><br>

            <li>Qualidade</li>
            Além de dormir 8 horas ininterruptas, é importante que você durma durante todas as fases do sono, incluindo o sono profundo REM e não-REM;
            isso exigiria um estudo do sono para confirmar, mas a qualidade do sono ainda é importante.<br><br>

            <li>Faça pausas durante os jogos</li>
            Para ajudar a prevenir o cansaço dos jogos, você precisará se afastar do videogame de sua escolha e fazer outra coisa - qualquer outra coisa. <br>
            Encontre algo de que goste e reserve um tempo longe da rotina. Ainda mais importante, agende um tempo longe do local de trabalho.<br>
            joga videogame principalmente em casa? Agende um tempo fora de casa a cada semana para fazer algo diferente. <br>
            Afastar-se fisicamente de tudo ajudará sua mente e seu corpo a lidar com o estresse do trabalho excessivo.<br><br>

            Fazer pausas inclui separação regular (isto é, tempo de inatividade programado semanalmente) e separação em estilo de férias de sua vida.<br>
            jogos são seu trabalho ou você gostaria que fosse. Portanto, você precisa tratá-lo como tal e tirar férias para descontrair e relaxar.<br><br>

            <li>Saúde Mental</li>
