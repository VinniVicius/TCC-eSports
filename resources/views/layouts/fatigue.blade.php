            <li>Desinteresse em atividades anteriormente agradáveis</li>
            Um sintoma comum da fadiga do jogo é perder o interesse na atividade que você gostava anteriormente.<br>
            Para streamers e concorrentes, isso geralmente significa perder o interesse em seu jogo principal ou em jogar videogames em geral.<br><br>
            Para os streamers, também pode incluir desinteresse na interação com os espectadores ou até mesmo ansiedade em relação a essa interação. <br>
            Independentemente disso, as coisas que você gostava anteriormente não são mais interessantes, e você pode começar a adivinhar seu grind na indústria de jogos.
