            Pensando nisso, recomendamos conversar com um profissional de saúde mental, tanto como medida preventiva quanto de tratamento.<br>
            As visitas regulares a um profissional de saúde mental são tão importantes quanto as visitas regulares ao seu médico ou dentista.<br><br>

