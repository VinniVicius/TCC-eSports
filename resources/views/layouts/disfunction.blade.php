            <br>
            <span>Curvando-se ou inclinado para a frente</span><br>
            Inclinar-se para frente aumenta a pressão na parte inferior das costas e tensiona os músculos ao redor das articulações SI.<br>
            Sentar-se ereto ou com a cadeira ligeiramente reclinada tira a pressão dos discos em suas costas e ajudará a reduzir o risco de dor postural nas
            costas e disfunção da articulação sacroilíaca.
            <br><br>
            <span>Sentado com as pernas fora de equilíbrio</span><br>
            Se suas pernas não estiverem equilibradas no assento, haverá tensão desigual na pélvis e nas articulações SI.<br>
            Mantenha ambas as coxas retas no assento!
            <br><br>
            <span>Sentado por muito tempo</span><br>
            A posição estática prolongada, mesmo em uma postura sentada "mais apropriada", pode eventualmente levar à disfunção da articulação sacroilíaca.<br id="Content4">
            Certifique-se de fazer pausas !

